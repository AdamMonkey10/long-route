// Look up the right entry for a station view.
// Order of preference:
//   <systemId>.<locationId>     (specific location)
//   <systemId>.concourse        (concourse view)
//   <systemId>                  (system-level fallback)
export function findGuideEntry(systemId, locationId) {
  if (locationId) {
    const specific = GUIDE_ENTRIES[`${systemId}.${locationId}`]
    if (specific) return { id: `${systemId}.${locationId}`, ...specific }
  } else {
    const concourse = GUIDE_ENTRIES[`${systemId}.concourse`]
    if (concourse) return { id: `${systemId}.concourse`, ...concourse }
  }
  const system = GUIDE_ENTRIES[systemId]
  if (system) return { id: systemId, ...system }
  return null
}
